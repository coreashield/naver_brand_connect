// 랜덤 접미사 선택 함수
export function getRandomSuffix() {
  return titleSuffixes[Math.floor(Math.random() * titleSuffixes.length)];
}

// 이전에 사용하지 않은 접미사 선택 (중복 방지)
export function getUniqueSuffix(usedSuffixes = new Set()) {
  const available = titleSuffixes.filter(s => !usedSuffixes.has(s));
  if (available.length === 0) {
    usedSuffixes.clear(); // 모두 사용했으면 리셋
    return titleSuffixes[Math.floor(Math.random() * titleSuffixes.length)];
  }
  const suffix = available[Math.floor(Math.random() * available.length)];
  usedSuffixes.add(suffix);
  return suffix;
}

